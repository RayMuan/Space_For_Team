package com.space4team.host.db;

public class HostDTO {
	private int h_num;
	private String h_name;
	private String h_email;
	private String h_id;
	private String h_pass;
	private String h_phone;
	private String h_birth;

	
	// get set
	public int getH_num() {
		return h_num;
	}
	public void setH_num(int h_num) {
		this.h_num = h_num;
	}
	public String getH_name() {
		return h_name;
	}
	public void setH_name(String h_name) {
		this.h_name = h_name;
	}
	public String getH_email() {
		return h_email;
	}
	public void setH_email(String h_email) {
		this.h_email = h_email;
	}
	public String getH_id() {
		return h_id;
	}
	public void setH_id(String h_id) {
		this.h_id = h_id;
	}
	public String getH_pass() {
		return h_pass;
	}
	public void setH_pass(String h_pass) {
		this.h_pass = h_pass;
	}
	public String getH_phone() {
		return h_phone;
	}
	public void setH_phone(String h_phone) {
		this.h_phone = h_phone;
	}
	public String getH_birth() {
		return h_birth;
	}
	public void setH_birth(String h_birth) {
		this.h_birth = h_birth;
	}
	
}//class
